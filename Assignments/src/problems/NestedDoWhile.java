package problems;

public class NestedDoWhile {

	public static void main(String[] args) {
		        int i = 1;
		        do {
		            int j = 1;
		            do {
		                System.out.println("Sai");
		                j++;
		            } while (j <=3);
		            System.out.println(i);
		            i++;
		        } while (i <=3);
		    }
		}


	
